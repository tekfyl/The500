int bfs(int s){
    vis[s] = 1;
    q.push(mk(s,-1));
    while(!q.empty()){
        s = q.front().fi; 
        for(int v:g[s]){
            if(!vis[v]){
                vis[v] = 1;
                q.push(mk(v,s));
            }
            else{
                if(v != q.front().se){
                    cout << v << " " << s;
                    cout << endl;
                    return 1;
                }
            }
        }
        q.pop();
    }
    return 0;
}


