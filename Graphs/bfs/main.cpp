using namespace std;

int s,n,v1,v2,m,i;
vi v,q;
vvi g(400);
vi vis(300,0);

void bfs(int s){
    vis[s] = 1;
    q.pb(s);
    while(!q.empty()){
        s = q.front(); q.erase(q.begin());
        cout << s << " ";
        for(auto v:g[s]){
            if(!vis[v]){
                vis[v] = 1;
                q.pb(v);
            }
        }
    }
}


int main(){
    cin >> n >> m;
    rep(i,m){
        cin >> v1 >> v2;
        g[v1].pb(v2);
        g[v2].pb(v1);
    }
    for(int i = 1; i<8; i++)
        if(!vis[i]) bfs(i);
    return 0;
}

