int dfs(int s, int p){
    vis[s] = 1;
    arr[s] = ++t;
    int at = arr[s];
    for(int v:g[s]){
        if(!vis[v]){
            dfs(v,s);
        }
        else{
            if(v != p){
                at = min(at, arr[v]);
            }
        }
    }
    if(at == arr[s] && p != -1 && p != 1){
        cout << p << " " << s << endl;
    }
    return at;
}


