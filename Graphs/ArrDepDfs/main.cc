#include <bits/stdc++.h>

#define all(c) c.begin(), c.end()
#define ll long long
#define pb push_back
#define vi vector<int>
#define vll vector<ll>
#define vvi vector< vi >
#define vvl vector< vector<ll> >
#define mk make_pair
#define ii pair <int, int>
#define LL pair <ll, ll>
#define fi first
#define se second
#define mx max_element
#define mn min_element
#define rep(i,n)    for(__typeof(n) i = 0; i < n; i++)
#define rep1(i,n)   for(__typeof(n) i = 1; i <= n; i++)

using namespace std;

int n,m,i,v1,v2,t=-1;
vi vis(500,0), gra,arr(8),dep(8);
vvi g(500);

void dfs(int s){
    vis[s] = 1;
    arr[s] = ++t;
    gra.pb(s);
    for(auto v:g[s]){
        if(!vis[v]){
            dfs(v);
        }
    }
    dep[s] = ++t;
}


int main(){
    cin >> n >> m;
    rep(i,m){
        cin >> v1 >> v2;
        g[v1].pb(v2);
        g[v2].pb(v1);
    }
    dfs(1);
    for(auto v:gra) cout << v << " ";
    cout << endl;
    for(int v:arr) cout << v << " ";
    cout << endl;
    for(int v:dep) cout << v << " ";
    cout << endl;
    return 0;
}

